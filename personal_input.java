package com.example.sidster.apiintegration;

public class personal_input {

    private String skills;

    private String mobile_no;

    private String name;

    private String links;

    private String location;

    private String email;

    public personal_input(String skills, String mobile_no, String name, String links, String location, String email) {
        this.skills = skills;
        this.mobile_no = mobile_no;
        this.name = name;
        this.links = links;
        this.location = location;
        this.email = email;
    }
}
